package examples;

public class Example {
    public static void main(String[] args) {


        // - byte -> b = 68
// - short -> s = 10000
// - int -> i = 2147483647
// - long -> l = 822337203
// - float -> f = 234.5f
// - double -> d = 123.4
// - boolean -> bo = true

        int myFirstInt = 5;
        System.out.println(myFirstInt);
        short myFirstShort = 12345;
        long myFirstLong = 1234567890;
        float myFirstFloat = 123.9f;
        double myFirstDouble = 123.4;
        boolean myFirstBoolean = true;
        System.out.println(myFirstBoolean);
        System.out.println(myFirstDouble);
        System.out.println(myFirstShort);
        System.out.println(myFirstLong);
        System.out.println(myFirstFloat);
        int i = 3;
        short s = 23456;
        long l = 112345678990l;
        float f = 123.1f;
        double d = 234.5;
        boolean b = false;
        System.out.println(i);
        System.out.println(s);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(i + myFirstInt);
    }
}
